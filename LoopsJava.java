
package loops.java;
public class LoopsJava {

    public static void main(String[] args) {
        int x = 1;
        int xx = 1;
        
        while (x <= 10) {
            System.out.println(x);
            x++;
            
            
        }
        
        do {
            System.out.println(xx);
            x++;
        } while (xx <= 10);
        
        
             }
    
    static {
    }
    
}
